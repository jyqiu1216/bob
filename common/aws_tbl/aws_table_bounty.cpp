int TbBounty::OnResponse(const Json::Value& item)
{
	oJsonWriter.omitEndingLineFeed();
	int dwResLen = 0;
	dwResLen = 0;
	this->Reset();
	Json::Value::Members vecMembers = item.getMemberNames();
	for (unsigned int i = 0; i < vecMembers.size(); ++i)
	{
 		if (vecMembers[i] == "uid")
		{
			m_nUid = strtoll(item["uid"]["N"].asString().c_str(), NULL, 10);
			continue;
		}
		if (vecMembers[i] == "next_refresh_time")
		{
			m_nNext_refresh_time = strtoll(item["next_refresh_time"]["N"].asString().c_str(), NULL, 10);
			continue;
		}
		if (vecMembers[i] == "star")
		{
			m_nStar = strtoll(item["star"]["N"].asString().c_str(), NULL, 10);
			continue;
		}
		if (vecMembers[i] == "s_stage")
		{
			if (FALSE == oJsonReader.parse(item["s_stage"]["S"].asString(),m_jS_stage))
			{
				m_jS_stage = Json::Value(Json::nullValue);
			}
			continue;
		}
		if (vecMembers[i] == "s_reward")
		{
			if (FALSE == oJsonReader.parse(item["s_reward"]["S"].asString(),m_jS_reward))
			{
				m_jS_reward = Json::Value(Json::nullValue);
			}
			continue;
		}
		if (vecMembers[i] == "base")
		{
			if (FALSE == oJsonReader.parse(item["base"]["S"].asString(),m_jBase))
			{
				m_jBase = Json::Value(Json::nullValue);
			}
			continue;
		}
		if (vecMembers[i] == "seq")
		{
			m_nSeq = strtoll(item["seq"]["N"].asString().c_str(), NULL, 10);
			continue;
		}
	}
	return 0;
}

TINT64 TbBounty::GetSeq()
{
	return m_nSeq;
}

