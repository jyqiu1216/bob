AwsMap* TbBounty::OnUpdateItemReq(
	const ExpectedDesc& expected_desc, int dwReturnValuesType, bool bReturnConsumedCapacity)
{
	oJsonWriter.omitEndingLineFeed();
	if (m_mFlag.size() == 0) //没有变化
	{
		return NULL;
	}
	AwsMap* pUpdateItem = new AwsMap;
	assert(pUpdateItem);
	string sBase64Encode;
	string sJsonEncode;
	bool bUpdateFlag = false;
	pUpdateItem->AddValue("/TableName", GetTableName());
	if (bReturnConsumedCapacity)
	{
		pUpdateItem->AddValue("/ReturnConsumedCapacity", "TOTAL");
	}
	if (dwReturnValuesType > RETURN_VALUES_NONE)
	{
		pUpdateItem->AddValue("/ReturnValues", ReturnValuesType2Str(dwReturnValuesType));
	}
	pUpdateItem->AddValue("/Key/uid/N", m_nUid);
	for (map<unsigned int, int>::iterator iter = m_mFlag.begin(); iter != m_mFlag.end(); ++iter)
	{
		bUpdateFlag = true;
		if (TbBOUNTY_FIELD_NEXT_REFRESH_TIME == iter->first)
		{
			if(UPDATE_ACTION_TYPE_DELETE == iter->second)
			{
				pUpdateItem->AddValue("/AttributeUpdates/next_refresh_time/Action", "DELETE");
			}
			else
			{
				pUpdateItem->AddValue("/AttributeUpdates/next_refresh_time/Value/N", m_nNext_refresh_time);
				pUpdateItem->AddValue("/AttributeUpdates/next_refresh_time/Action", UpdateActionType2Str(iter->second));
			}
			continue;
		}
		if (TbBOUNTY_FIELD_STAR == iter->first)
		{
			if(UPDATE_ACTION_TYPE_DELETE == iter->second)
			{
				pUpdateItem->AddValue("/AttributeUpdates/star/Action", "DELETE");
			}
			else
			{
				pUpdateItem->AddValue("/AttributeUpdates/star/Value/N", m_nStar);
				pUpdateItem->AddValue("/AttributeUpdates/star/Action", UpdateActionType2Str(iter->second));
			}
			continue;
		}
		if (TbBOUNTY_FIELD_S_STAGE == iter->first)
		{
			if (!m_jS_stage.empty())
			{
				pUpdateItem->AddValue("/AttributeUpdates/s_stage/Value/S", JsonEncode(oJsonWriter.write(m_jS_stage), sJsonEncode));
				pUpdateItem->AddValue("/AttributeUpdates/s_stage/Action", "PUT");
			}
			else
			{
				pUpdateItem->AddValue("/AttributeUpdates/s_stage/Action", "DELETE");
			}
			continue;
		}
		if (TbBOUNTY_FIELD_S_REWARD == iter->first)
		{
			if (!m_jS_reward.empty())
			{
				pUpdateItem->AddValue("/AttributeUpdates/s_reward/Value/S", JsonEncode(oJsonWriter.write(m_jS_reward), sJsonEncode));
				pUpdateItem->AddValue("/AttributeUpdates/s_reward/Action", "PUT");
			}
			else
			{
				pUpdateItem->AddValue("/AttributeUpdates/s_reward/Action", "DELETE");
			}
			continue;
		}
		if (TbBOUNTY_FIELD_BASE == iter->first)
		{
			if (!m_jBase.empty())
			{
				pUpdateItem->AddValue("/AttributeUpdates/base/Value/S", JsonEncode(oJsonWriter.write(m_jBase), sJsonEncode));
				pUpdateItem->AddValue("/AttributeUpdates/base/Action", "PUT");
			}
			else
			{
				pUpdateItem->AddValue("/AttributeUpdates/base/Action", "DELETE");
			}
			continue;
		}
		if (TbBOUNTY_FIELD_SEQ == iter->first)
		{
			if(UPDATE_ACTION_TYPE_DELETE == iter->second)
			{
				pUpdateItem->AddValue("/AttributeUpdates/seq/Action", "DELETE");
			}
			else
			{
				pUpdateItem->AddValue("/AttributeUpdates/seq/Value/N", m_nSeq);
				pUpdateItem->AddValue("/AttributeUpdates/seq/Action", UpdateActionType2Str(iter->second));
			}
			continue;
		}
		assert(0);
	}
	if(bUpdateFlag)
	{
		++m_nSeq;
		pUpdateItem->AddValue("/AttributeUpdates/seq/Value/N", m_nSeq);
		pUpdateItem->AddValue("/AttributeUpdates/seq/Action", "PUT");
	}
	ostringstream oss;
	for (unsigned int i = 0; i < expected_desc.vecExpectedItem.size(); ++i)
	{
		const ExpectedItem& item = expected_desc.vecExpectedItem[i];
		FieldDesc& fld_desc = oTableDesc.mFieldDesc[item.udwFldNo];
		oss.str("");
		oss << "/Expected/" << fld_desc.sName << "/Exists";
		pUpdateItem->AddBoolean(oss.str(), item.bExists);
		if (item.bExists) //Exists为true时才需要Value
		{
			oss.str("");
			oss << "/Expected/" << fld_desc.sName << "/Value/" << DataType2Str(fld_desc.dwType);
			if (FIELD_TYPE_N == fld_desc.dwType)
			{
				pUpdateItem->AddValue(oss.str(), item.nValue);
				continue;
			}
			if (FIELD_TYPE_S == fld_desc.dwType)
			{
				pUpdateItem->AddValue(oss.str(), item.sValue);
				continue;
			}
			//FIELD_TYPE_B型不能expected
			assert(0);
		}
	}
	return pUpdateItem;
}

